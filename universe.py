def flat(universe: dict) -> list[tuple[str, str, str]]:
    """{섹터:[(티커,이름)]} → [(티커, 이름, 섹터)] 평탄화."""
    out = []
    for sector, items in universe.items():
        for tk, name in items:
            out.append((tk, name, sector))
    return out


def get_universe(market: str) -> list[tuple[str, str, str]]:
    """market: 'kr' | 'us' | 'all'"""
    market = market.lower()
    if market == "kr":
        return flat(KR_UNIVERSE)
    if market == "us":
        return flat(US_UNIVERSE)
    return flat(KR_UNIVERSE) + flat(US_UNIVERSE)
